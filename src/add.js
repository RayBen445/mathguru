/**
 * add.js
 * Adds two numbers together.
 */

/**
 * Returns the sum of two numbers.
 *
 * @param {number} a - The first number.
 * @param {number} b - The second number.
 * @returns {number} The sum of a and b.
 *
 * @example
 * const { add } = require('mathguru');
 * add(3, 5); // 8
 */
function add(a, b) {
  return a + b;
}

module.exports = add;
